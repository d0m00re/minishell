#include <stdlib.h>

void	ft_str2d_del(char ***str)
{
	int c;

	if (!str || !(*str) || !(**str))
		return ;
	c = 0;
	while (str[0][c])
	{
		free(str[0][c]);
		c++;
	}
	free(str[0]);
	str[0] = 0;
}
